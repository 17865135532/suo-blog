/**
 * @param {number[]} nums
 * @param {number} target
 * @return {number}
 */
var searchInsert = function(nums, target) {
  var hi = nums.length - 1,
    lo = 0

  // 2.迭代，换界
  while (hi - lo >= 0) {
    var mi = (hi + lo) >>> 1
    if (target < nums[mi]) {
      hi = mi - 1
    } else if (nums[mi] < target) {
      lo = mi + 1
    } else {
      return mi
    }
  }
  return lo
}
searchInsert([1, 3, 5, 6], 2)
